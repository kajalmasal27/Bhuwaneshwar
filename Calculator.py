def addition(a,b):
    add =  a+b
    print(f"Addition of {a} and {b} is {add}.")

def subtraction(a,b):
    sub = a-b
    print(f"Addition of {a} and {b} is {sub}.")

def multiplication(a,b):
    mul = a*b
    print(f"Multiplication  of {a} and {b} is {mul}.")

